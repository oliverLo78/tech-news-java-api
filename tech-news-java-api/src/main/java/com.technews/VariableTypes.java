package src.main.java.com.technews;

public class VariableTypes {
    public static void main(String[] args) {
        // Java is a statically typed language when you declare a variable
        // you must tell the computer what type of data it will hold
        String name = "Cleopatra";
        int age = 2016 + 69;
        double price = 1999.99;
        boolean truth = true;
        boolean falseness = !truth;
    }
}
